week = int(input("Welcome to the Days of the Week calculator. Enter a number (1-7) : "))
if week == 1:
    print("Monday")
elif week == 2:
    print("Tuesday")
elif week == 3:
    print("Wednesday")
elif week == 4:
    print("Thursday")
elif week == 5:
    print("Friday")
elif week == 6:
    print("Saturday")
elif week == 7:
    print("Sunday")
else:
    print("Please Enter a number from 1 to 7. Thanks!")